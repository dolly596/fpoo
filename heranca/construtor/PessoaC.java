package heranca.construtor;

public class PessoaC {
	private String nome;
	private String telefone;
	
	
	public PessoaC(String nome, String telefone) {
		
		this.nome = nome;
		this.telefone = telefone;
	}


	public String getNome() {
		return nome;
	}

	public String getTelefone() {
		return telefone;
	}


	
}
